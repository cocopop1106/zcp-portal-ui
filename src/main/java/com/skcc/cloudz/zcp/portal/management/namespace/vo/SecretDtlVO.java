package com.skcc.cloudz.zcp.portal.management.namespace.vo;

public class SecretDtlVO {
	
	private String server;
	private String username;
	private String password;
	private String email;
	private String crtFile;
	private String crtPath;
	private String keyFile;
	private String keyPath;
	private String type;
	private String label;
	
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCrtFile() {
		return crtFile;
	}
	public void setCrtFile(String crtFile) {
		this.crtFile = crtFile;
	}
	public String getCrtPath() {
		return crtPath;
	}
	public void setCrtPath(String crtPath) {
		this.crtPath = crtPath;
	}
	public String getKeyFile() {
		return keyFile;
	}
	public void setKeyFile(String keyFile) {
		this.keyFile = keyFile;
	}
	public String getKeyPath() {
		return keyPath;
	}
	public void setKeyPath(String keyPath) {
		this.keyPath = keyPath;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
}
